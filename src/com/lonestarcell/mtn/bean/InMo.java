package com.lonestarcell.mtn.bean;

public class InMo {
	
	private String username, mmoId, amount, msisdn, acctRef, currency;
	
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getMmoId() {
		return mmoId;
	}
	public void setMmoId(String mmoId) {
		this.mmoId = mmoId;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getMsisdn() {
		return msisdn;
	}
	public void setMsisdn(String msisdn) {
		this.msisdn = msisdn;
	}
	public String getAcctRef() {
		return acctRef;
	}
	public void setAcctRef(String acctRef) {
		this.acctRef = acctRef;
	}
	public String getCurrency() {
		return currency;
	}
	public void setCurrency(String currency) {
		this.currency = currency;
	}

	
	
	
	
	
	

}
